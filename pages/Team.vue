  mounted() {
    document.body.classList.add('light-theme');
    const scrollToElement = document.getElementById('team__inner');
    const scrollButton = document.querySelector('.team__btn i');

    // Функция для прокрутки к элементу
    function scrollToteam() {
      scrollToElement.scrollIntoView({ behavior: 'smooth' }); // Прокрутка с плавным эффектом
    }

    const scrollToElement1 = document.querySelector('.eight__inner');
    const scrollButton1 = document.querySelector('.team__btn h2');

    // Функция для прокрутки к элементу
    function scrollToteam1() {
      scrollToElement1.scrollIntoView({ behavior: 'smooth' }); // Прокрутка с плавным эффектом
    }

    // Добавляем обработчик события на клик по кнопке
    scrollButton.addEventListener('click', scrollToteam);
    scrollButton1.addEventListener('click', scrollToteam1);

    if (window.innerWidth > 1024) {
      let teamList = document.querySelectorAll(".team-list")
      teamList.forEach(item => {
        let teamImg = item.querySelectorAll('.team-img')
        teamImg.forEach(img => {
          img.addEventListener('mouseover', () => {
            teamImg.forEach(otherImg => {
              otherImg.classList.add('small');
            });
            img.classList.remove('small');
            img.classList.add('big');
          })
          img.addEventListener('mouseout', () => {
            teamImg.forEach(otherImg => {
              otherImg.classList.remove('small');
              otherImg.classList.remove('big');
            });
          })
        });
      });
    }

  }
}

