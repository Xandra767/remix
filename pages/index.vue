      sixTitles.forEach((item) => {
        function onScroll() {
          if (isElementNearBottom(item, 100)) {
            item.classList.remove("anim");
          } else {
            item.classList.add("anim");
          }
        }

        document.addEventListener("scroll", onScroll);
      });
